"""Print all arithmetic operations with 27.5 as first number and 2 as second number"""

print("27.5 + 2 = " + str(27.5 + 2))
print("27.5 - 2 = " + str(27.5 - 2))
print("27.5 * 2 = " + str(27.5 * 2))
print("27.5 / 2 = " + str(27.5 / 2))
print("27.5 // 2 = " + str(27.5 // 2))
print("27.5 ** 2 = " + str(27.5 ** 2))